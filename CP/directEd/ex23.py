# Define a list named my_list with initial values
my_list = [3, 1, 4, 2, 20, 10, 5, 6, 7]

# Append the value 20 to the end of my_list
my_list.append(20)

# Extend my_list with the values [100, 200, 300]
my_list.extend([100, 200, 300])

# Insert the value 60 at index 5 in my_list
my_list.insert(5, 60)

# Remove the value 300 from my_list
my_list.remove(300)

# Sort my_list in ascending order
my_list.sort()

# Remove and return the last element from my_list
my_list.pop()

# Print the final contents of my_list
print(my_list)

